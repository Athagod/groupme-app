const mongoose = require("mongoose");

const EventSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    location: {
        type: String,
        required: true,
    },
    image: {
        type: String,
    },
    host: {
        type: mongoose.Schema.Types.ObjectId,
        required: true,
        ref: 'user'
    },
    responses: [
        {
            going: [
                {
                  user: {
                    type: mongoose.Schema.Types.ObjectId,
                    ref: "user",
                  },
                },
              ],
            invited: [
                {
                  user: {
                    type: mongoose.Schema.Types.ObjectId,
                    ref: "user",
                  },
                },
              ],
            maybe: [
                {
                  user: {
                    type: mongoose.Schema.Types.ObjectId,
                    ref: "user",
                  },
                },
              ],
         },
      ],
      comments: [
        {
          user: {
            type: mongoose.Schema.Types.ObjectId,
            ref: "user",
          },
          text: {
            type: String,
            required: true,
          },
          avatar: {
            type: String,
          },
          date: {
            type: Date,
            default: Date.now,
          },
        },
      ],
});

module.exports = Event = mongoose.model("event", EventSchema);
